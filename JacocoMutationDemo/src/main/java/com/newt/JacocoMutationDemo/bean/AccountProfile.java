package com.newt.JacocoMutationDemo.bean;

import java.util.Date;

public class AccountProfile {
	
	private int account_no;
	
	private String userid;
	
	private String password;
	
	private String account_holder_name;
	
	private String account_name;
	
	private String phone_no;
	
	private String email_address;
	
	private String address;
	
	private Date account_start_date;
	
	private String account_nick_name;

	public int getAccount_no() {
		return account_no;
	}

	public void setAccount_no(int account_no) {
		this.account_no = account_no;
	}

	
	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getAccount_holder_name() {
		return account_holder_name;
	}

	public void setAccount_holder_name(String account_holder_name) {
		this.account_holder_name = account_holder_name;
	}
	
	public String getAccount_name() {
		return account_name;
	}

	public void setAccount_name(String account_name) {
		this.account_name = account_name;
	}

	public String getPhone_no() {
		return phone_no;
	}

	public void setPhone_no(String phone_no) {
		this.phone_no = phone_no;
	}

	public String getEmail_address() {
		return email_address;
	}

	public void setEmail_address(String email_address) {
		this.email_address = email_address;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public Date getAccount_start_date() {
		return account_start_date;
	}

	public void setAccount_start_date(Date account_start_date) {
		this.account_start_date = account_start_date;
	}

	public String getAccount_nick_name() {
		return account_nick_name;
	}

	public void setAccount_nick_name(String account_nick_name) {
		this.account_nick_name = account_nick_name;
	}
	

	
}


	
